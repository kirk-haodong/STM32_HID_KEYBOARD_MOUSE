#include "main.h"
#include "usb_device.h"
#include "app.h"

#define PRESS 1
#define LOOSE 0
#define SEND_KEYBOARD 2
#define SEND_MOUSE 3

TIM_HandleTypeDef htim2;
TIM_HandleTypeDef htim3;
TIM_HandleTypeDef htim9;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_TIM2_Init(void);
static void MX_TIM3_Init(void);
static void MX_TIM9_Init(void);
static int STATE = LOOSE;
uint8_t hid_Buffer [9]={0x01,0,0,0,0,0,0,0,0};
uint8_t hid_init_Buffer [9]={0x10,0,0,0,0,0,0,0,0};

int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_USB_DEVICE_Init();
  MX_TIM3_Init();
  MX_TIM9_Init();
  HAL_TIM_Base_Start_IT(&htim2);
	HAL_TIM_Base_Start_IT(&htim3);
	HAL_TIM_Base_Start_IT(&htim9);
	
  while (1)
  {			
		switch(STATE)
		{
			case LOOSE:
					STATE = LOOSE;
					break;
			case PRESS:
					Control();
					STATE = LOOSE;
					break;
			case SEND_KEYBOARD:
					HAL_GPIO_TogglePin(GPIOB,GPIO_PIN_7);
					USBD_HID_SendReport (&hUsbDeviceFS, hid_Buffer,9);
					STATE = LOOSE;
					break;
			case SEND_MOUSE:
					USBD_HID_SendReport (&hUsbDeviceFS,hid_init_Buffer ,9);
					HAL_GPIO_TogglePin(GPIOB,GPIO_PIN_5);
					STATE = LOOSE;
					break;
			default:
					STATE = LOOSE;
					break;}
	}
	
}

